
package classes;


public class MemberType {
    
   private final String name;
   private final double price;

    public MemberType(String name, double price) {
        this.name = name;
        this.price = price;
        
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }
   
   
   
}
